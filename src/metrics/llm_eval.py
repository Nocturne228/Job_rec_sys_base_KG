"""
LLM-as-a-Judge evaluation for generation layer quality.

Evaluates career advice generated by LangGraph + LLM along three dimensions:
  - relevance: does the advice address the user's skill gaps?
  - feasibility: are the recommendations actionable with concrete resources?
  - hallucination: does the advice contain non-existent or misleading resources?

Uses a rule-based evaluator (fast, no LLM API) and an optional LLM-as-a-Judge
mode (needs an external LLM, e.g. Qwen, to judge output quality).

Corresponds to RecSys.md §11.4.
"""

from __future__ import annotations

import json
import re
from dataclasses import dataclass, field
from typing import Dict, List, Optional, Any, Callable


# ============================================================================
# Evaluation criteria
# ============================================================================

_EVAL_DIMENSIONS = [
    "relevance",      # Does it address user's skill gaps?
    "feasibility",    # Are the recommendations actionable?
    "hallucination",  # Does it contain non-existent resources? (lower = better)
    "format",         # Is the output valid structured JSON?
    "specificity",    # Does it give specific resources / timelines?
]


@dataclass
class EvaluationCriteria:
    """Scoring rubric for each dimension."""
    dimension: str
    weight: float
    description: str


DEFAULT_CRITERIA: List[EvaluationCriteria] = [
    EvaluationCriteria("relevance",   0.30, "Addresses user's skill gaps"),
    EvaluationCriteria("feasibility", 0.25, "Recommendations are actionable"),
    EvaluationCriteria("hallucination", 0.20, "No non-existent resources"),
    EvaluationCriteria("format",      0.15, "Valid JSON with required fields"),
    EvaluationCriteria("specificity", 0.10, "Specific resources and timelines"),
]


@dataclass
class EvalResult:
    """Evaluation result for a single generated output."""
    user_id: str
    job_id: str
    scores: Dict[str, float] = field(default_factory=dict)
    weighted_score: float = 0.0
    flagged: bool = False         # hallucination score > threshold
    notes: str = ""


# ============================================================================
# Rule-based evaluator (no external LLM needed)
# ============================================================================

class RuleBasedScorer:
    """Fast heuristic scorer without external LLM API calls."""

    @staticmethod
    def score_format(response: str) -> float:
        """0-1: valid JSON with required fields."""
        try:
            data = json.loads(response)
            score = 0.6  # valid JSON
            required = ["summary", "learning_path", "critical_skills"]
            found = sum(1 for f in required if f in data)
            score += 0.4 * (found / len(required))
            return min(score, 1.0)
        except (json.JSONDecodeError, TypeError):
            return 0.1

    @staticmethod
    def score_relevance(response: str, skill_gaps: List[str]) -> float:
        """0-1: mentions user's missing skills."""
        if not skill_gaps:
            return 0.5
        text = response.lower()
        matched = sum(1 for gap in skill_gaps if gap.lower() in text)
        return min(matched / len(skill_gaps), 1.0)

    @staticmethod
    def score_feasibility(response: str) -> float:
        """0-1: contains concrete action items (courses, docs, timeframes)."""
        indicators = ["course", "tutorial", "documentation", "practice",
                      "project", "month", "week", "learn", "study"]
        text = response.lower()
        count = sum(1 for ind in indicators if ind in text)
        return min(count / max(len(indicators), 1), 1.0)

    @staticmethod
    def score_hallucination(response: str) -> float:
        """0-1: lower is better. Penalizes URL patterns, fabricated stats."""
        # Simple heuristics: URLs and very specific numbers suggest hallucination
        url_count = len(re.findall(r'https?://\S+', response))
        stat_patterns = re.findall(r'\d{2,3}%', response)
        score = min(1.0, (url_count * 0.3 + len(stat_patterns) * 0.2))
        return score

    @staticmethod
    def score_specificity(response: str) -> float:
        """0-1: contains named resources, concrete timelines."""
        has_timeline = bool(re.search(r'\d+[- ]?(week|month|day)', response, re.I))
        has_resources = bool(re.search(r'(course|tutorial|book|docs|udemy|coursera|fast\.ai)',
                                       response, re.I))
        has_skills_mention = bool(re.search(r'[A-Z][a-z]+(?:\s[A-Z][a-z]+)*(?:\s\d+\.?\d*)?', response))
        score = (0.4 * int(has_timeline) + 0.4 * int(has_resources) + 0.2 * int(has_skills_mention))
        return min(score, 1.0)


# ============================================================================
# LLM-as-a-Judge evaluator
# ============================================================================

LLM_JUDGE_PROMPT = """You are an impartial judge evaluating career advice quality.

User skill gaps: {skill_gaps}
Generated advice: {response}

Rate on a scale of 1-5 (integer only) for each:
1. Relevance: Does it address the user's skill gaps?
2. Feasibility: Are recommendations actionable with concrete resources?
3. Hallucination: Does it contain non-existent/misleading info? (1=none, 5=severe)

Respond with JSON: {{"relevance": N, "feasibility": N, "hallucination": N}}
"""


class LLMJudgeEvaluator:
    """
    Evaluates generated career advice quality.

    Two modes:
    - "rule": rule-based heuristic scoring (default, no API needed)
    - "llm": uses an external LLM as the judge (requires llm_client callback)

    Usage (rule mode):
        evaluator = LLMJudgeEvaluator(criteria=DEFAULT_CRITERIA)
        result = evaluator.evaluate("user_001", "job_001", response_text, skill_gaps=["Python", "SQL"])
        print(f"Overall score: {result.weighted_score:.2f}")
    """

    def __init__(self, criteria: Optional[List[EvaluationCriteria]] = None):
        self.criteria = criteria or DEFAULT_CRITERIA
        self.scorer = RuleBasedScorer()
        self._history: List[EvalResult] = []

    def evaluate(self, user_id: str, job_id: str,
                 response: str,
                 skill_gaps: Optional[List[str]] = None,
                 mode: str = "rule",
                 llm_client: Optional[Callable[[str], str]] = None,
                 ) -> EvalResult:
        """Evaluate a single generated output."""
        skill_gaps = skill_gaps or []
        scores: Dict[str, float] = {}

        if mode == "llm" and llm_client is not None:
            scores = self._llm_judge(llm_client, response, skill_gaps)
        else:
            scores = self._rule_scores(response, skill_gaps)

        # Weighted score
        weights = {c.dimension: c.weight for c in self.criteria}
        # Flip hallucination (lower is better → higher is better for aggregation)
        flipped: Dict[str, float] = {}
        for dim, val in scores.items():
            if dim == "hallucination":
                flipped[dim] = 1.0 - val
            else:
                flipped[dim] = val

        weighted = sum(flipped.get(d.dimension, 0) * weights.get(d.dimension, 0)
                       for d in self.criteria)
        flagged = scores.get("hallucination", 0) > 0.6

        result = EvalResult(
            user_id=user_id,
            job_id=job_id,
            scores=scores,
            weighted_score=round(weighted, 3),
            flagged=flagged,
        )
        self._history.append(result)
        return result

    def batch_evaluate(self, samples: List[Dict[str, Any]],
                       mode: str = "rule") -> List[EvalResult]:
        """Evaluate multiple samples."""
        results = []
        for s in samples:
            r = self.evaluate(
                user_id=s.get("user_id", ""),
                job_id=s.get("job_id", ""),
                response=s.get("response", ""),
                skill_gaps=s.get("skill_gaps", []),
                mode=mode,
                llm_client=s.get("llm_client"),
            )
            results.append(r)
        return results

    def summary(self) -> Dict[str, Any]:
        """Aggregate summary across all evaluations."""
        if not self._history:
            return {"count": 0}

        n = len(self._history)
        avg_weighted = sum(r.weighted_score for r in self._history) / n
        flagged_rate = sum(1 for r in self._history if r.flagged) / n

        dim_avgs: Dict[str, float] = {}
        for dim in _EVAL_DIMENSIONS:
            vals = [r.scores.get(dim, 0) for r in self._history]
            avg_val = sum(vals) / len(vals) if vals else 0
            dim_avgs[dim] = round(avg_val, 3)

        return {
            "count": n,
            "avg_weighted_score": round(avg_weighted, 3),
            "flagged_rate": round(flagged_rate, 3),
            "dimension_averages": dim_avgs,
            "json_compliance": round(
                sum(1 for r in self._history if r.scores.get("format", 0) >= 0.8) / n
                if n else 0, 3
            ),
        }

    # ----- internals -----

    @staticmethod
    def _rule_scores(response: str, skill_gaps: List[str]) -> Dict[str, float]:
        s = RuleBasedScorer()
        return {
            "relevance": round(s.score_relevance(response, skill_gaps), 3),
            "feasibility": round(s.score_feasibility(response), 3),
            "hallucination": round(s.score_hallucination(response), 3),
            "format": round(s.score_format(response), 3),
            "specificity": round(s.score_specificity(response), 3),
        }

    @staticmethod
    def _llm_judge(llm_client: Callable[[str], str],
                    response: str,
                    skill_gaps: List[str]) -> Dict[str, float]:
        """Call external LLM for evaluation. Returns scores {dimension: 0-1}."""
        prompt = LLM_JUDGE_PROMPT.format(
            skill_gaps=", ".join(skill_gaps),
            response=response[:2000],  # truncate to reasonable length
        )
        raw = llm_client(prompt)
        try:
            data = json.loads(raw)
            return {
                "relevance": min(int(data.get("relevance", 1)), 5) / 5,
                "feasibility": min(int(data.get("feasibility", 1)), 5) / 5,
                "hallucination": min(int(data.get("hallucination", 1)), 5) / 5,
            }
        except (json.JSONDecodeError, ValueError, TypeError):
            # Fallback to rule-based
            return LLMJudgeEvaluator._rule_scores(response, skill_gaps)
